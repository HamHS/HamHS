package com.example.myapplication.Kotlin

// 내맘대로 원하는것을 넣을 수 있는 상자 -> Variable(var)
// 한번 넣으면 바꿀 수 없는 상자 -> Value(val)
// var/val 변수명(상자) = 값(넣고싶은것)
//Variable/Value

var num = 10
var hello = "hello"

val point = 20

fun main(){
    println(num)
    println(hello)
    println(point)
}